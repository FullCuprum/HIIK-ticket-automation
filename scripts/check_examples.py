parser = get_ticket_parser()
output_lines: list[str] = []
for index, text in enumerate(CASES, start=1):
    result = parser.parse(text)
    output_lines.append(f"=== CASE {index} ===")
    output_lines.append(
        json.dumps(
            {key: result[key] for key in [
                "building",
                "location",
                "problem_description",
                "ticket_type",
                "priority",
                "required_skill",
                "event_datetime",
                "missing_fields",
            ]},
            ensure_ascii=False,
            indent=2,
        )
    )
    output_lines.append("")

result_path = Path(__file__).with_name("check_examples_output.txt")
result_path.write_text("\n".join(output_lines), encoding="utf-8")
print(result_path.read_text(encoding="utf-8"))
